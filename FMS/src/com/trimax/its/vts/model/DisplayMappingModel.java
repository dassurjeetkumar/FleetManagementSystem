package com.trimax.its.vts.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="pis_display_platfrom")
public class DisplayMappingModel {
	@Id
	@GeneratedValue
	@Column(name="pis_display_platform_id")
	private int pis_display_platform_id;
	@Column(name="display_unit_id")
	private int display_unit_id;
	@Column(name="floor_id")
	private int floor_id;
	@Column(name="bay_id")
	private int bay_id;
	@Column(name="bus_station_id")
	private int bus_station_id;
	@Column(name="platform_id")
	private int platform_id;
	@Column(name="created_by")
    private int created_by;
	@Column(name="created_date")
    private String created_date;
	public int getPis_display_platform_id() {
		return pis_display_platform_id;
	}
	public void setPis_display_platform_id(int pis_display_platform_id) {
		this.pis_display_platform_id = pis_display_platform_id;
	}
	public int getDisplay_unit_id() {
		return display_unit_id;
	}
	public void setDisplay_unit_id(int display_unit_id) {
		this.display_unit_id = display_unit_id;
	}
	public int getFloor_id() {
		return floor_id;
	}
	public void setFloor_id(int floor_id) {
		this.floor_id = floor_id;
	}
	public int getBay_id() {
		return bay_id;
	}
	public void setBay_id(int bay_id) {
		this.bay_id = bay_id;
	}
	public int getBus_station_id() {
		return bus_station_id;
	}
	public void setBus_station_id(int bus_station_id) {
		this.bus_station_id = bus_station_id;
	}
	public int getPlatform_id() {
		return platform_id;
	}
	public void setPlatform_id(int platform_id) {
		this.platform_id = platform_id;
	}
	public int getCreated_by() {
		return created_by;
	}
	public void setCreated_by(int created_by) {
		this.created_by = created_by;
	}
	public String getCreated_date() {
		return created_date;
	}
	public void setCreated_date(String created_date) {
		this.created_date = created_date;
	}
}
