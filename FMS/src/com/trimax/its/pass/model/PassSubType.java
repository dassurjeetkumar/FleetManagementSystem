package com.trimax.its.pass.model;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

	@Entity
	@Table(name="pass_sub_type")
	public class PassSubType {
	@Id
	@GeneratedValue
	@Column(name="pass_subtype_id")
	private int pass_subtype_id;

	@Column(name="sub_type_name")
	private String sub_type_name;
	@Column(name="pass_type_id")
	private int pass_type_id;

	@Column(name="status")
	private String status;

	@Column(name="remarks")
	private String remarks;

	@Column(name="created_by")
	private int created_by;

	@Column(name="created_date")
	private Date created_date;

	@Column(name="updated_by")
	private int updated_by;

	@Column(name="updated_date")
	private Date updated_date;

	public int getPass_subtype_id() {
		return pass_subtype_id;
	}

	public void setPass_subtype_id(int pass_subtype_id) {
		this.pass_subtype_id = pass_subtype_id;
	}

	public String getSub_type_name() {
		return sub_type_name;
	}

	public void setSub_type_name(String sub_type_name) {
		this.sub_type_name = sub_type_name;
	}

	public int getPass_type_id() {
		return pass_type_id;
	}

	public void setPass_type_id(int pass_type_id) {
		this.pass_type_id = pass_type_id;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public int getCreated_by() {
		return created_by;
	}

	public void setCreated_by(int created_by) {
		this.created_by = created_by;
	}

	public Date getCreated_date() {
		return created_date;
	}

	public void setCreated_date(Date created_date) {
		this.created_date = created_date;
	}

	public int getUpdated_by() {
		return updated_by;
	}

	public void setUpdated_by(int updated_by) {
		this.updated_by = updated_by;
	}

	public Date getUpdated_date() {
		return updated_date;
	}

	public void setUpdated_date(Date updated_date) {
		this.updated_date = updated_date;
	}
	
	
	
}
