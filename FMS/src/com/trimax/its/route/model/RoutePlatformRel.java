package com.trimax.its.route.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="route_platform_rel")
public class RoutePlatformRel {

	@Id
	@GeneratedValue
	@Column(name="route_platform_id")
	private int route_platform_id;
	
	@Column(name="route_id")
	private int route_id;
	
	@Column(name="bus_stop_id")
	private int bus_stop_id;
	
	@Column(name="depo_id")
	private int depo_id;
	
	@Column(name="floor_id")
	private int floor_id;
	
	@Column(name="bay_id")
	private int bay_id;
	
	@Column(name="platform_id")
	private int platform_id;
	
	@Column(name="service_type_id")
	private int service_type_id;
	
	@Column(name="status")
	private String status;

	public int getRoute_platform_id() {
		return route_platform_id;
	}

	public void setRoute_platform_id(int route_platform_id) {
		this.route_platform_id = route_platform_id;
	}

	public int getRoute_id() {
		return route_id;
	}

	public void setRoute_id(int route_id) {
		this.route_id = route_id;
	}

	public int getBus_stop_id() {
		return bus_stop_id;
	}

	public void setBus_stop_id(int bus_stop_id) {
		this.bus_stop_id = bus_stop_id;
	}

	public int getDepo_id() {
		return depo_id;
	}

	public void setDepo_id(int depo_id) {
		this.depo_id = depo_id;
	}

	public int getFloor_id() {
		return floor_id;
	}

	public void setFloor_id(int floor_id) {
		this.floor_id = floor_id;
	}

	public int getBay_id() {
		return bay_id;
	}

	public void setBay_id(int bay_id) {
		this.bay_id = bay_id;
	}

	public int getPlatform_id() {
		return platform_id;
	}

	public void setPlatform_id(int platform_id) {
		this.platform_id = platform_id;
	}

	public int getService_type_id() {
		return service_type_id;
	}

	public void setService_type_id(int service_type_id) {
		this.service_type_id = service_type_id;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}
	
}
